def add(a, b):
    print("adding ", a, " and ", b)
    return a + b
def sub(a, b):
    print("subtract ", a, " and ", b)
    return a - b
def mul(a, b):
    print("multiply ", a, " and ", b)
    return a * b
def div(a, b):
    print("divide ", a, " and ", b)
    return a / b
